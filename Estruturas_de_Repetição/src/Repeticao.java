
public class Repeticao {
	
	public static void main(String[] args) {
		//While:
		int i = 0;
		while (i < 5) {
		  System.out.println(i);
		  i++;
		}
		
		//Do while
		int j = 0;
		do {
		  System.out.println(j);
		  j++;
		}
		while (j < 5);
		
		//For
		
		for (int k = 0; k < 5; k++) {
			  System.out.println(k);
			}
	}
}
